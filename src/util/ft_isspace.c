#include "minitalk.h"

int     ft_isspace(int c)
{
    if (c == 32)
        return (1);
    if (c == 9)
        return (1);
    if (c == 10)
        return (1);
    if (c == 11)
        return (1);
    if (c == 12)
        return (1);
    if (c == 13)
        return (1);
    return (0);
}
